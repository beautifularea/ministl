/*
 标准关联容器：
 set / map 
 衍生出： multiset/multimap - 》基于 RB-tree实现
 
 unordered_map : 基于hashtable实现
 
 非标准：
 hast_set/hash_map 基于hashtable实现
 
 树的基本知识：
 1、二叉搜索树
 任何节点的键值，大于其左子树，小于其右子树 
 
        中
      /    \
     小     大
 
 2、平衡二叉树 AVLtree , RB-tree
 3、AVL tree
 四中情况：
 left-left/left-right/right-left/right-right
 单旋转和双旋转
 
 4、RB-tree
 1、不是红就是黑
 2、跟为黑
 3、红节点的子节点必须是黑
 4、黑节点必须相同   
 
 */

